public class ex01 {
    public static void main(String[] args) {
        final int limite = 20;
        //programa que muestre por pantalla los 20 
        //primeros números naturales (1, 2, 3,…., 20)
        for (int i=1; i <= limite; i++) { //indicamos el valor inicial, el limite y el incremento de uno en uno
            System.out.println(i); //mostramos por pantalla
        }
    }
}
